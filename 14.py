# What IF

people = 20
cats = 30
dogs = 15


if people < cats:
    print("Too many cats")

if people > cats:
    print("Not many cats")

if people < dogs:
    print("The world is drooled on !")

if people > dogs:
    print("The world is dry!")

dogs += 5

if people >= dogs:
    print("People are greater than or equal to dogs.")

if people <= dogs:
    print("People are lesser than or equal to dogs.")

if people == dogs:
    print("People are equal to dogs")